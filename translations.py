def t(key: str, lang: str) -> str:
    """
    Translates a given key into the specified language.
    Falls back to English if the key or language is not found.
    Logs a warning if the key is entirely missing.
    """
    if key in TRANSLATIONS:
        if lang in TRANSLATIONS[key]:
            return TRANSLATIONS[key][lang]
        elif "en" in TRANSLATIONS[key]:
            logger.warning(f"Missing translation for key '{key}' in language '{lang}'. Falling back to English.")
            return TRANSLATIONS[key]["en"] # Fallback to English
    logger.warning(f"Missing translation key '{key}' entirely. Returning key as is.") # Log if key is completely absent
    return key # Return key itself if no translation found